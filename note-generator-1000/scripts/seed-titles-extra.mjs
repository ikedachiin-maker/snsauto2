// 既存のtitles.jsonを読み込む
const outputPath = path.join(ROOT, 'data', 'titles.json');
const existing = JSON.parse(fs.readFileSync(outputPath, 'utf-8'));
let allNotes = [...existing.notes];

const targets = {
  fukugyou: 300,
  mindset: 200,
  renai: 200,
  business: 150,
  health: 150,
};

const extraByGenre = {
  fukugyou: fukugyouExtra,
  mindset: mindsetExtra,
  renai: renaiExtra,
  business: businessExtra,
  health: healthExtra,
};

let currentId = allNotes.length + 1;

for (const [genre, targetCount] of Object.entries(targets)) {
  const existing = allNotes.filter(n => n.genre === genre);
  const needed = targetCount - existing.length;

  if (needed <= 0) {
    console.log(`${genre}: 既に${existing.length}本 (目標${targetCount}本) -> スキップ`);
    continue;
  }

  const pool = extraByGenre[genre];
  const added = [];

  for (let i = 0; i < needed && i < pool.length; i++) {
    added.push({
      id: currentId++,
      title: pool[i],
      genre,
      angle: ANGLE_BY_INDEX(i),
      emotion: EMOTION_BY_INDEX(i),
      target_length: LENGTH_BY_INDEX(i),
      price: PRICE_BY_INDEX(i),
    });
  }

  allNotes.push(...added);
  console.log(`${genre}: ${added.length}本追加 -> 合計${allNotes.filter(n => n.genre === genre).length}本`);
}

fs.writeFileSync(outputPath, JSON.stringify({ notes: allNotes }, null, 2), 'utf-8');

console.log(`\ntitles.json を更新しました。合計: ${allNotes.length}本`);
for (const [genre, target] of Object.entries(targets)) {
  const count = allNotes.filter(n => n.genre === genre).length;
  console.log(`  ${genre}: ${count}本 (目標${target}本)`);
}
