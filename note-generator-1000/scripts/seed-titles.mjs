const ANGLE_BY_INDEX = (idx) => ANGLES[idx % ANGLES.length];
const EMOTION_BY_INDEX = (idx) => EMOTIONS[idx % EMOTIONS.length];
const PRICE_BY_INDEX = (idx) => PRICES[idx % PRICES.length];
const LENGTH_BY_INDEX = (idx) => 3000 + (idx % 6) * 1000;

function buildNotes(titles, genre, startId) {
  return titles.map((title, i) => ({
    id: startId + i,
    title,
    genre,
    angle: ANGLE_BY_INDEX(i),
    emotion: EMOTION_BY_INDEX(i),
    target_length: LENGTH_BY_INDEX(i),
    price: PRICE_BY_INDEX(i),
  }));
}

